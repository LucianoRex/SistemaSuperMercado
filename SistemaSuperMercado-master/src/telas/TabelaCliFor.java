    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        cadastro.model.setNumRows(0);
        model.setNumRows(0);
        Connection con = ConexaoMySQL.getConnection();              
        String[] cTipo = new String[3];
        cTipo[0] = "CLIENTE_FORNECEDOR.CCLIENTE_FORNECEDOR";
        cTipo[1] = "CLIENTE_FORNECEDOR.RAZAO_SOCIAL";
        cTipo[2] = "CLIENTE_FORNECEDOR.NOME";
        String[] cParametro = new String[4];
        cParametro[0] = "=";
        cParametro[1] = "<>";
        cParametro[2] = ">";
        cParametro[3] = "<";
        int tipo = jCTipo.getSelectedIndex();
        int parametro = jCParametro.getSelectedIndex();
        String search = jTSearch.getText();

        String sql = "SELECT * FROM CLIENTE_FORNECEDOR"               
                + " WHERE " + cTipo[tipo] + " " + cParametro[parametro]+" "+search;
        System.out.println(sql);

        try {
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                String linha[] = {rs.getString("CCLIENTE_FORNECEDOR"), rs.getString("RAZAO_SOCIAL"), rs.getString("NOME")};
                model.addRow(linha);
                cadastro.model.addRow(linha);
                System.out.println(rs.getString("CCLIENTE_FORNECEDOR"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "" + ex.getMessage());
        }

            //STEP 5: Extract data from result set

    }//GEN-LAST:event_btnSearchActionPerformed

