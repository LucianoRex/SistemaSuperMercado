    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
      String login=jTLogin.getText();
      String senha = jPassword.getText();
      boolean match = false;
      
      String sql="SELECT USUARIO.USUARIO,USUARIO.SENHA"
              + " FROM USUARIO";
      
      try{
          Connection con = ConexaoMySQL.getConnection();
          PreparedStatement ps = con.prepareStatement(sql);
          ResultSet rs = ps.executeQuery();
          
          while(rs.next()){
              if(rs.getString("USUARIO").equals(login)&& rs.getString("SENHA").equals(senha)){
                  match=true;
                  break;
              }
          }
      }catch(SQLException ex){
          JOptionPane.showMessageDialog(this, ex.getMessage());
      }
      if(match){
          Principal principal = new Principal();
          principal.setVisible(true);
      }else{
          JOptionPane.showMessageDialog(this, "Acesso negado");
      }
    }//GEN-LAST:event_btnOkActionPerformed

