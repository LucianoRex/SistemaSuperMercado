    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (btnEditar.isSelected()) {
            editando = true;
            btnCancelar.setEnabled(true);
            btnEditar.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/Ok-20.png")));
            btnExcluir.setEnabled(false);
            btnInserir.setEnabled(false);
            btnOk.setEnabled(false);
            jTCod.setEnabled(true);
            jTDescricao.setEnabled(true);
            jTQtde.setEnabled(true);
            jTvalor.setEnabled(true);

            totalizador();

        } else {
            editando = false;
            btnCancelar.setEnabled(false);
            btnEditar.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/Edit-20.png")));
            btnExcluir.setEnabled(true);
            btnInserir.setEnabled(true);
            btnOk.setEnabled(false);

            totalizador();
            int i = jTBProdutos.getSelectedRow();
            model.setValueAt(jTCod.getText(), i, 0);
            model.setValueAt(jTDescricao.getText(), i, 1);
            model.setValueAt(jTQtde.getText(), i, 2);
            model.setValueAt(jTvalor.getText(), i, 3);
            jTCod.setEnabled(false);
            jTDescricao.setEnabled(false);
            jTQtde.setEnabled(false);
            jTvalor.setEnabled(false);

        }
        //
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (jTBProdutos.getRowCount() == 1) {
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
        }
        model.removeRow(jTBProdutos.getSelectedRow());

    }//GEN-LAST:event_btnExcluirActionPerformed

