    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        String cod = jTCod.getText();
        String descricao = jTDescricao.getText();
        String valor = jTvalor.getText();
        String qtde = jTQtde.getText();
        String total = Double.toString(Double.parseDouble(valor) * Double.parseDouble(qtde));

        String linha[] = {cod, descricao, valor, qtde, total};
        model.addRow(linha);
        jTCod.setText("");
        jTDescricao.setText("");
        jTvalor.setText("");
        jTQtde.setText("");
        jTCod.requestFocus();
        jTCod.setEnabled(false);
        jTDescricao.setEnabled(false);
        jTQtde.setEnabled(false);
        jTvalor.setEnabled(false);
        totalizador();
        inserir = false;
        btnCancelar.setEnabled(false);
        btnOk.setEnabled(false);
        if (jTBProdutos.getRowCount() > 0) {
            btnEditar.setEnabled(true);
            btnExcluir.setEnabled(true);
        }

    }//GEN-LAST:event_btnOkActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if (editando) {
            btnExcluir.setEnabled(true);
            btnInserir.setEnabled(true);
            btnOk.setEnabled(true);
            jTCod.setEnabled(false);
            jTDescricao.setEnabled(false);
            jTQtde.setEnabled(false);
            jTvalor.setEnabled(false);
            int linha = jTBProdutos.getSelectedRow();
            String cod = (String) jTBProdutos.getValueAt(linha, 0);
            jTCod.setText(cod);
            String descricao = (String) jTBProdutos.getValueAt(linha, 1);
            jTDescricao.setText(descricao);
            String preco = (String) jTBProdutos.getValueAt(linha, 2);
            jTvalor.setText(preco);
            String qtde = (String) jTBProdutos.getValueAt(linha, 3);
            jTQtde.setText(qtde);
            btnEditar.doClick();
            btnCancelar.setEnabled(false);
            totalizador();
        } else {
            btnCancelar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnOk.setEnabled(false);
            jTCod.setText("");
            jTDescricao.setText("");
            jTvalor.setText("");
            jTQtde.setText("");
            jTCod.setEnabled(false);
            jTDescricao.setEnabled(false);
            jTQtde.setEnabled(false);
            jTvalor.setEnabled(false);
            inserir = false;
            totalizador();
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

