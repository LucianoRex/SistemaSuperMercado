    private void totalizador() {
        String total = "";
        String qtde = "";
        double tot = 0;
        int countLinha = jTBProdutos.getRowCount();
        for (int i = 0; i < countLinha; i++) {
            total = (String) jTBProdutos.getValueAt(i, 4);
            qtde = (String) jTBProdutos.getValueAt(i, 3);
            tot += Double.parseDouble(total);
        }
        System.out.println(countLinha);
        jLtotal.setText(Double.toString(tot));
    }
